    'Anindita' => 17, 'Yuvanesh' => 34, 'Rishabh' => 35, 'Perumal' => 37,
    'Maari' => 38, 'Bala' => 2, 'Dhanush' => 13, 'Ranjini' => 27,
    'Tamil Arasan' => 12, 'Deeksha' => 25, 'Kishore Prabakaran' => 51,
    'Anaz' => 18, 'Gargi Bisht' => 57, 'Swapna M' => 55, 'Sivaranjani N' => 58,
    'Saran' => 44, 'Meghana' => 45, 'Sooraj' => 19, 'Ayush' => 4,
    'Shoyab' => 32, 'Sneha Prathap' => 42, 'Akshara' => 61, 'Fida Taneem' => 41,
];

if (($argv[1] ?? '') === 'php') {
    $q = fn ($s) => "'" . str_replace("'", "\\'", $s) . "'";
    $lines = ['<?php', '',
        '// Auto-generated by database/data/parse-kpis-html.php from public/kpis.html (do not hand-edit).',
        '// KPI scorecards seeded into kpi_scorecard_items by KpiScorecardSeeder.',
        '// Shape: [users.id => [ [name, description, target|null, weight], ... ]].',
        'return ['];
    foreach ($out as $p) {
        if (! isset($map[$p['name']])) { fwrite(STDERR, "UNMAPPED: {$p['name']}\n"); continue; }
        if (empty($p['kpis'])) { fwrite(STDERR, "NO KPIS: {$p['name']}\n"); continue; }
        $lines[] = "    {$map[$p['name']]} => [ // {$p['name']}";
        foreach ($p['kpis'] as $k) {
            $t = $k['target'] === null ? 'null' : $q($k['target']);
            $w = $k['weight'] === null ? 'null' : $k['weight'];
            $lines[] = "        ['name' => {$q($k['name'])}, 'description' => {$q($k['description'])}, 'target' => {$t}, 'weight' => {$w}],";
        }
        $lines[] = '    ],';
    }
    $lines[] = '];';
    echo implode("\n", $lines) . "\n";
    exit;
}

echo json_encode($out, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES) . "\n\n=== SUMMARY ===\n";
foreach ($out as $p) {
    $sum = array_sum(array_map(fn ($k) => $k['weight'] ?? 0, $p['kpis']));
    echo str_pad($p['name'], 24) . ' id=' . ($map[$p['name']] ?? '??') . ' kpis=' . count($p['kpis']) . " weightSum={$sum}\n";
}
