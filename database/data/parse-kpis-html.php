<?php
/**
 * Generator for database/data/kpi_scorecards.php.
 *
 * Parses the static reference doc public/kpis.html — for each `.person` block it
 * reads the person name and the first `.matrix-table`, extracting each KPI row's
 * name, description, monthly target and weight (the last/“Total” column), while
 * skipping the Total / "Per Product" subtotal rows.
 *
 * Usage (from repo root):
 *   php database/data/parse-kpis-html.php          # human-readable JSON + summary
 *   php database/data/parse-kpis-html.php php > database/data/kpi_scorecards.php
 *
 * After regenerating, re-run:  php artisan db:seed --class=KpiScorecardSeeder
 */

$html = file_get_contents(__DIR__ . '/../../public/kpis.html');
$doc = new DOMDocument();
libxml_use_internal_errors(true);
$doc->loadHTML('<?xml encoding="utf-8" ?>' . $html);
libxml_clear_errors();
$xp = new DOMXPath($doc);

$txt = fn (?DOMNode $n) => $n ? trim(preg_replace('/\s+/', ' ', $n->textContent)) : '';

$out = [];
foreach ($xp->query("//div[@class='person']") as $person) {
    $name = $txt($xp->query(".//div[@class='person-name']", $person)->item(0));
    $table = $xp->query(".//table[contains(@class,'matrix-table')]", $person)->item(0);
    if (! $name || ! $table) {
        continue;
    }
    $rows = [];
    foreach ($xp->query(".//tbody/tr", $table) as $tr) {
        $tds = $xp->query("./td", $tr);
        if ($tds->length < 2) {
            continue;
        }
        $first = $tds->item(0);
        $divs = $xp->query("./div", $first);
        $kname = $divs->length ? $txt($divs->item(0)) : $txt($first);
        // Skip summary / subtotal rows ("Total", "Subtotal · Per Product", "Per Product").
        if ($kname === '' || preg_match('/^(total|subtotal|per product)\b/i', $kname)) {
            continue;
        }
        $desc = $divs->length > 1 ? $txt($divs->item(1)) : '';
        $target = $txt($tds->item(1));
        if (in_array($target, ['—', '-', ''], true)) {
            $target = null;
        }
        preg_match('/\d+/', $txt($tds->item($tds->length - 1)), $m);
        $weight = isset($m[0]) ? (int) $m[0] : null;
        $rows[] = ['name' => $kname, 'description' => $desc, 'target' => $target, 'weight' => $weight];
    }
    $out[] = ['name' => $name, 'kpis' => $rows];
}

// kpis.html person-name -> users.id (verified against the live roster).
$map = [
